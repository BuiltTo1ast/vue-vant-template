    // 加载数据
    async init() {
      const loading = this.$loading()
      const { gid } = this.$route.query
      if (!gid) {
        return
      }
      const resp = await detail(gid).catch(() => {})
      loading.close()
      this.detail = resp.data
    },

    async openPrevious() {
      this.$router.go(-1)
    },

    async onConfirmBuy() {
      const { gid } = this.$route.query
      if (!gid) return
      this.$router.push({ name: 'OrderConfirm', query: { gid }})
    }
